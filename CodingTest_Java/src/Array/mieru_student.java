package Array;

import java.util.Scanner;

public class mieru_student {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt();
		
		int[] arr = new int[n];
		
		for(int i = 0; i < n ; i++) {
			arr[i] = sc.nextInt();
		}
		
		int cnt = 1; // 맨 앞사람은 무조건 보이니까!
		
		int max = arr[0]; // 맨 앞사람으로 초기화
		
		for(int i = 0; i < n ; i++) {
			if(max<arr[i]) {
				cnt++;
				max = arr[i];
			}
		}
		System.out.println(cnt);
		
}
}
